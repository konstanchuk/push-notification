namespace Konstanchuk\PushNotification\Block\Adminhtml\Template\Edit;

use Konstanchuk\PushNotification\Block\Adminhtml\GenericButton;


class SaveAndContinueButton extends GenericButton
{
    /**
     * @return array
     */
